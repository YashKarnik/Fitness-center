    <h4 class='display-4 '> Overview</h4>
    <p>
    The biceps curl targets explicitly the biceps brachii, which is located on the front part of the arm between the shoulder and the elbow. This muscle's primary function is elbow flexion and forearm supination. The biceps also has two heads known as the long head and the short head.[2] The biceps brachii during the radioulnar joint (forearm) supinating phase is a stronger elbow flexor. The biceps brachii during the elbow flexing phase is a stronger forearm supinator.[3] During the elbow flexion phase, motor units in the lateral portion of the long head of the biceps are preferentially activated. Still, during the forearm rotation phase, motor units in the medial portion are preferentially activated.


